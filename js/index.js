//Percorra o JSON e insera os nomes dos membros na lista html
function percorrerMembros() {
  //escreva a lÃ³gica
  let membros = document.getElementById("membros")

  
  objData.members.forEach(member => {
    let lista=`
    <ul>
      <li>Nome: ${member.name}</li>
      <li>Idade: ${member.age}</li>
      <li>IdentidadeSecreta: ${member.secretIdentity}</li>
      <li>Powers:
        <ul>
          ${member.powers.map(power =>`<li>${power}</li>`).join("")}       
        </ul>  
      </li>
    </ul>
    `
    
    let novaDiv = document.createElement("div");
    novaDiv.innerHTML = lista;
    novaDiv.className ="item";
    membros.appendChild(novaDiv)
  });

}
